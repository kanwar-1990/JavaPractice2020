
public class forloop
{
	public static void main(String[] args) {

		
//		main method is void --ncz it never returns a value   
		
		System.out.println("FOR LOOP STARTED");

		for (int i = 1; i <= 100; i++) 
		{
			System.out.println("print i in an increamental order " +i);
		}
		System.out.println("loop is done");
	
	int one ='A'/'A'; //1
	
	for(int i=one; i<='d';i+=one) // d in ascii code is 100 
	{
		System.out.println(i);
	}
	
	}

}
